    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeButtonActionPerformed
        int index;
        // Create a integer named Index.
        
        index = Collections.binarySearch(cd, cdInput.getText());
        //To get the text from the text field and search it in the collections.
        
        if (index> -1)
        {
            cd.remove(cdInput.getText());
        }
        else
        {
            outputArea.setText("CD not found in the collections.");
        }
        // If there has the same song as the user type in the textfield
        // It will remove it from the collections
        // As long as there don't have the same song in the collections
        // It will output that "can not find in the collections."
    }//GEN-LAST:event_removeButtonActionPerformed

