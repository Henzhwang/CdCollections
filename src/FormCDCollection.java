    private void displayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayButtonActionPerformed
        outputArea.setText("Orginal Order\n");
        //Outout the title of order.
        
        for (int i = 0; i < cd.size(); i++)
        {
            outputArea.setText(outputArea.getText() + cd.get(i) + "\n");
        }
        //To out put the cds in the collections line by line
        
        outputArea.setText(outputArea.getText() + "\n\n" + "Sorted Order\n");
        //Creating another sort of order.
        
        Collections.sort(cd);
        //System.out.println(cd);
        //Sort the cds in Alphabetical order.
        
        for (int i = 0; i < cd.size(); i++)
        {
            outputArea.setText(outputArea.getText() + cd.get(i) + "\n");
        }
        //out put the cds in Alphabetical order.
    }//GEN-LAST:event_displayButtonActionPerformed
    
    
